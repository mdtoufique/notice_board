package com.example.myapplication;

public class Asd {
    String t;
    String p;
    String m;


    public Asd(String m, String p, String t)
    {
        this.m=m;
        this.p=p;
        this.t=t;
    }

    public String getP() {
        return p;
    }

    public String getM() {
        return m;
    }

    public String getT() {
        return t;
    }

    public void setP(String p) {
        this.p = p;
    }

    public void setM(String m) {
        this.m = m;
    }

    public void setT(String t) {
        this.t = t;
    }
}
